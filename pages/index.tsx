import Head from 'next/head'
import styles from '@/styles/Layout.module.scss'
import { Col, Row } from 'antd'
import { Card } from 'antd';
import Earn from '@/components/Earn'
import { GetServerSideProps } from 'next';
import { generateRandomWalletAddress } from '@/lib/address';
import prisma from '@/lib/prisma';
import { Address } from '@prisma/client';

export const getServerSideProps: GetServerSideProps = async () => {
  const randomKey = generateRandomWalletAddress()
  const address = await prisma.address.create({
    data: {
      address: randomKey,
      balance: 0
    }
  })
  return {
    props: { address },
  };
};

export default function Home(props: {address: Address}) {
  return (
    <>
      <Head>
        <title>WD Baas</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/crypto.png" />
      </Head>
      <main className={styles.main}>
        <Row gutter={16}>
          <Col span={12}>
            <Card title="Earn Money" bordered={false}>
              <Earn {...props.address}/>
            </Card>
          </Col>
          <Col span={12}>
            <Card title="Wallet" bordered={false}>
              Card content
            </Card>
          </Col>
        </Row>
      </main>
    </>
  )
}
